          않았는데도 물건이 대량으로 빠져 있었다. 수산물 코너에서 만난 정은미(43)씨도 건어물과 해산물을 여러 개씩
          담고 있었다. 정씨는 “어제 원전 오염수 방류를 실시간 영상으로 보니까 ‘정말 하는구나’ 실감이 났다”며 “사재기
