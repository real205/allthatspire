          증가세가 나타난 셈이다. 또 다른 대형마트에서도 비슷한 양상이 나타났다. 전체 수산물 매출은 전년 동일 대비
          약 15%, 건해산물 매출은 약 40% 늘었다. 멸치와 미역으로 좁히면 각각 150%, 180% 증가하면서 2.5~2.8배 매출
          급등세를 보였다. 소금 매출은 전년 동일 대비 3.5배나 뛰었다. 오래 보관할 수 있는 상품들에 대한 수요 급증이
          확인된다. 온라인에서도 수산물·건어물 주문량이 증가하는 추세다. 이커머스 전문기업 커넥트웨이브의 가격 비교
          서비스 다나와에 따르면 원전 오염수 방류 소식이 전해지던 지난 17~23일 소금 거래액이 전주 동기 대비 238%
          증가했다. 특히 국내산 천일염의 지난 23일 거래액은 일주일 전보다 431% 상승했다. 다만 지난 6월과 같은
          품귀현상까지는 아직 빚어지지 않고 있다. 유통업계 한 관계자는 “소비자들의 불안 심리가 수산물과 건어물 매출
          급등으로 확인되는 듯하다. 이 같은 현상이 얼마만큼 지속될 지 예의주시하면서 대응책을 마련하고 있다”며
